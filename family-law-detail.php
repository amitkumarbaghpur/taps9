                     <h3>Joint Petition shall contain in Mutual Consent Divorce India</h3>
                     <p>A joint petition signed by both parties is required to be filed before the Court of the Principal Judge, Family Court.</p>
                     <p>The Joint Petition shall &nbsp;contain the following</p>
                     <ol>
                        <li>Date of marriage</li>
                        <li>Date of separation (there should be one year separation prior to filing the divorce Petition, One year separation would mean &nbsp;the parties have not been living as partners &nbsp;for a year or more , therefore &nbsp;it does not mean they should be &nbsp;living separately at separate addresses.</li>
                        <li>There is only one ground on which Mutual consent divorce is filed i.e Temperamental differences. Therefore the Petition will not contain any&nbsp; allegations against each other which makes the whole process extremely clean and simple.</li>
                        <li>In case there are children from the said marriage, then the Petition will state the date of birth of the child/children. The sole custody of the child/children will remain with whatever the parties have decided mutually . The other party will have visitation rights to visit the child.</li>
                        <li>In case the parties have decided on certain payments to be made before the divorce is granted both parties can decide the time when such payment will be made , the same can also be made at the time of hearing in the form of online payment or bank draft.</li>
                        <li>After these points are jotted down in the petition both parties are required to sign the joint petition alongwith marriage proofs in the form of Marriage photographs / Wedding Card / Marriage Certificate.</li>
                        <li>After the Joint petition is submitted both parties are required to be present before the Court of the Principal Judge, Family Court for the first motion for both Motions. There is a gap of 6 months between two motions, However if there is a separation of more than 18 months , parties can approach the Court with a waiver application for waiving the cooling period.</li>
